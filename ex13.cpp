// Concept: For Loop
// Reference: Ch 2
// Why: The for loop provides a compact syntax for counter-controlled iteration.

#include <iostream>

int main() {
    for (int i = 0; i < 5; ++i) {
        std::cout << i << " ";
    }
    return 0;
}

